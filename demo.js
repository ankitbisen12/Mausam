const cityName = (city) => {
  if (city.split(" ").length === 1) {
    return city.slice(0, 1).toUpperCase() + city.substring(1).toLowerCase();
  } else {
    return city
      .split(" ")
      .map((ct) => ct.charAt(0).toUpperCase() + ct.toLowerCase().substring(1))
      .join(" ");
  }
};

